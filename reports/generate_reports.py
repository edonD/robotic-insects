def embed_image(image_path):
    """Embed PNG as base64 data URL."""
    image_path = Path(image_path)
    if not image_path.exists():
        return None

    with open(image_path, 'rb') as f:
        data = base64.b64encode(f.read()).decode('utf-8')
    return f"data:image/png;base64,{data}"

def create_metrics_table(metrics_list):
    """Create HTML table from metrics list."""
    html = '<table>\n<tr><th>Metric</th><th>Actual</th><th>Spec</th><th>Status</th></tr>\n'

    for metric_name, actual, spec, status in metrics_list:
        status_class = 'metric-good' if status == 'PASS' else 'metric-bad'
        status_symbol = '✓' if status == 'PASS' else '✗'
        html += f'<tr><td>{metric_name}</td><td class="{status_class}">{actual}</td><td>{spec}</td><td class="{status_class}">{status_symbol} {status}</td></tr>\n'

    html += '</table>'
    return html

def generate_report(module_name, module_description, metrics, plot_paths, notes, status='PASS'):
    """Generate HTML report for a module."""

    # Status banner
    status_class = 'status-pass' if status == 'PASS' else 'status-fail'
    status_symbol = '✓✓✓' if status == 'PASS' else '✗✗✗'
    status_banner = f'<div class="status-banner {status_class}">{status_symbol} {status}</div>'

    # Metrics table
    metrics_html = f'<div class="section"><h2>Key Metrics</h2>\n{create_metrics_table(metrics)}\n</div>'

    # Plots
    plots_html = '<div class="section"><h2>Plots & Visualizations</h2>\n'
    for plot_title, plot_path in plot_paths:
        img_data_url = embed_image(plot_path)
        if img_data_url:
            plots_html += f'<div class="plot-container"><img src="{img_data_url}" alt="{plot_title}" /><div class="plot-caption">{plot_title}</div></div>\n'
    plots_html += '</div>'

    # Notes
    notes_html = f'<div class="section"><h2>Notes</h2>\n<p>{notes}</p>\n</div>'

    # Combine sections
    sections = metrics_html + plots_html + notes_html

    # Final HTML
    html = HTML_TEMPLATE.format(
        title=f"{module_name} — Report",
        module_name=module_name,
        module_description=module_description,
        status_banner=status_banner,
        sections=sections,
    )

    return html

